  MM.t = function (key, params) {
    let s = MM.I18N[MM.lang][key];
    if (s == null) s = MM.I18N.de[key];
    if (s == null) return key;
    if (params) {
      for (const k in params) {
        s = s.split('{' + k + '}').join(String(params[k]));
      }
    }
    return s;
  };

